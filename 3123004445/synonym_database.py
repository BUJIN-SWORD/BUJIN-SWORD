def get_synonyms(word: str) -> List[str]:
    """
    获取词语的所有同义词（整合四大词库，优先匹配语境）
    :param word: 待查询词语
    :return: 包含自身的同义词列表
    """
    # 整合所有词库
    all_synonyms = {**THEME_SYNONYMS, **ROLE_SYNONYMS, **SCENE_SYNONYMS, **OBJECT_SYNONYMS}
    
    # 精确匹配词库
    for base_word, synonyms in all_synonyms.items():
        if word == base_word or word in synonyms:
            return synonyms.copy()
    
    # 若未匹配到，返回自身（确保兼容性）
    return [word]